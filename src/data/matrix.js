export function getDistance(from, to) {
  if (from === to) return 0
  const key1 = `${from}|${to}`
  const key2 = `${to}|${from}`
  if (MATRIX_DATA[key1] !== undefined) return MATRIX_DATA[key1]
  if (MATRIX_DATA[key2] !== undefined) return MATRIX_DATA[key2]
  return null
}

export function getAllLocationNames() {
  const names = new Set()
  for (const key of Object.keys(MATRIX_DATA)) {
    const [from, to] = key.split("|")
    names.add(from)
    names.add(to)
  }
  return [...names].sort()
}